package com.math;

public class Math_Methods {
    public static void main(String[] args) {
        System.out.println("Max : " + Math.max(34.03, 78.56));
        System.out.println("Min : " + Math.min(45.03, 45.003));
        System.out.println("Absolute : " + Math.abs(-15));
        System.out.println("Round off : " + Math.round(14.45));
        System.out.println("Round off : " + Math.round(14.55));
        System.out.println("Power or exponential : " + Math.pow(5,4));
        System.out.println("Sroot : " + Math.sqrt(9));
        System.out.println("Sroot : " + Math.sqrt(450));
        System.out.println("Ceil : " + Math.ceil(14.23));
        System.out.println("Floor : " + Math.floor(14.23));
        System.out.println("Random : " + Math.random());

    }
}
