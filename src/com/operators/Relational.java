package com.operators;

public class Relational {
    public static void main(String[] args){

        int num1 = 6; int num2 = 8;
        System.out.println(num1 == num2);
        System.out.println(num1 != num2);
        System.out.println(num1 > num2);
        System.out.println(num1 < num2);
        System.out.println(num1 >= num2);
        System.out.println(num1 <= num2);
        System.out.println("hi");
    }
}
